/**
 * Schema Comparator - Compares two database schemas and generates migration scripts
 */
class SchemaComparator {
    constructor(sourceSchema, destSchema, options = {}) {
        this.source = sourceSchema;
        this.dest = destSchema;
        this.migrationScript = [];
        this.options = {
            detectDrops: options.detectDrops || false,
            preserveColumnOrder: options.preserveColumnOrder || false,
            ...options
        };
        this.stats = {
            tablesAdded: 0,
            tablesDropped: 0,
            columnsAdded: 0,
            columnsModified: 0,
            columnsDropped: 0,
            indexesAdded: 0,
            manualPkTables: 0
        };
    }

    compare() {
        this.migrationScript.push('-- ----------------------------------------------------------------');
        this.migrationScript.push('-- Migration Script Generated by JS DDL Tool');
        this.migrationScript.push('-- Goal: Apply changes to Destination to match Source');
        this.migrationScript.push(`-- Generated: ${new Date().toLocaleString()}`);
        this.migrationScript.push('-- ----------------------------------------------------------------\n');

        // 1. Missing Tables (in source but not in dest)
        for (const table in this.source) {
            if (!this.dest[table]) {
                this.stats.tablesAdded++;
                this.migrationScript.push(`-- [MISSING TABLE] ${table}`);
                this.migrationScript.push(this.source[table].full_create_stmt);
                this.migrationScript.push('');
            } else {
                this._compareTables(table);
            }
        }

        // 2. Dropped Tables (in dest but not in source) - if enabled
        if (this.options.detectDrops) {
            for (const table in this.dest) {
                if (!this.source[table]) {
                    this.stats.tablesDropped++;
                    this.migrationScript.push(`-- [TABLE TO DROP] ${table}`);
                    this.migrationScript.push(`DROP TABLE IF EXISTS \`${table}\`;`);
                    this.migrationScript.push('');
                }
            }
        }

        if (this.migrationScript.length <= 5) {
            this.migrationScript.push('-- No schema differences found. Databases match!');
        }

        return this.migrationScript.join('\n');
    }

    _compareTables(tableName) {
        const srcTable = this.source[tableName];
        const dstTable = this.dest[tableName];
        const changes = [];
        const deferredAutoIncrementModifications = [];
        const keyChanges = [];

        // 2. Missing Columns or Type Mismatches
        for (const colName in srcTable.columns) {
            const srcColData = srcTable.columns[colName];

            if (!dstTable.columns[colName]) {
                // Missing Column
                this.stats.columnsAdded++;
                const hasAutoIncrement = /\bAUTO_INCREMENT\b/i.test(srcColData.definition);
                const safeDefinition = hasAutoIncrement
                    ? this._stripAutoIncrement(srcColData.definition)
                    : srcColData.definition;

                let addStmt = `ADD COLUMN \`${colName}\` ${safeDefinition}`;

                // Add column position if preserveColumnOrder is enabled
                if (this.options.preserveColumnOrder && srcTable.columnOrder) {
                    const colIndex = srcTable.columnOrder.indexOf(colName);
                    if (colIndex > 0) {
                        const prevCol = srcTable.columnOrder[colIndex - 1];
                        addStmt += ` AFTER \`${prevCol}\``;
                    } else if (colIndex === 0 && srcTable.columnOrder.length > 1) {
                        addStmt += ' FIRST';
                    }
                }

                changes.push(addStmt);

                // MySQL requires AUTO_INCREMENT columns to be indexed at all times.
                // In an ALTER TABLE statement, clauses are processed in order, so adding an AUTO_INCREMENT
                // column before its key exists can fail. We add it without AUTO_INCREMENT first, then
                // upgrade it after key changes in the same ALTER TABLE.
                if (hasAutoIncrement) {
                    deferredAutoIncrementModifications.push(`MODIFY COLUMN \`${colName}\` ${srcColData.definition}`);
                }
            } else {
                // Check Type and Default Value
                const dstColData = dstTable.columns[colName];
                const srcClean = this._normalizeType(srcColData.definition);
                const dstClean = this._normalizeType(dstColData.definition);

                // Compare default values
                const srcDefault = this._normalizeDefault(srcColData.default);
                const dstDefault = this._normalizeDefault(dstColData.default);

                if (srcClean !== dstClean || srcDefault !== dstDefault) {
                    this.stats.columnsModified++;
                    changes.push(`MODIFY COLUMN \`${colName}\` ${srcColData.definition}`);
                }
            }
        }

        // 3. Dropped Columns (in dest but not in source) - if enabled
        if (this.options.detectDrops) {
            for (const colName in dstTable.columns) {
                if (!srcTable.columns[colName]) {
                    this.stats.columnsDropped++;
                    changes.push(`DROP COLUMN \`${colName}\``);
                }
            }
        }

        // 4. Handle PRIMARY KEY changes (must be done before other indexes)
        const srcPrimaryKey = this._extractPrimaryKey(srcTable.indexes);
        const dstPrimaryKey = this._extractPrimaryKey(dstTable.indexes);

        const pkColumnChangeManual =
            Boolean(srcPrimaryKey && dstPrimaryKey) &&
            this._normalizePrimaryKeyIndex(srcPrimaryKey) !== this._normalizePrimaryKeyIndex(dstPrimaryKey);

        if (pkColumnChangeManual) {
            this.stats.manualPkTables++;
            this._emitManualPrimaryKeyMigrationNotice(tableName, dstPrimaryKey, srcPrimaryKey);
        }

        if (!pkColumnChangeManual) {
            if (srcPrimaryKey && dstPrimaryKey) {
                // Both have PRIMARY KEY - check if they differ
                const srcPKNorm = this._normalizePrimaryKeyIndex(srcPrimaryKey);
                const dstPKNorm = this._normalizePrimaryKeyIndex(dstPrimaryKey);
                if (srcPKNorm !== dstPKNorm) {
                    // PRIMARY KEY definition changed - must drop old one first
                    keyChanges.push('DROP PRIMARY KEY');
                    keyChanges.push(`ADD ${srcPrimaryKey}`);
                }
            } else if (srcPrimaryKey && !dstPrimaryKey) {
                // Source has PRIMARY KEY but destination doesn't
                keyChanges.push(`ADD ${srcPrimaryKey}`);
            } else if (!srcPrimaryKey && dstPrimaryKey && this.options.detectDrops) {
                // Source doesn't have PRIMARY KEY but destination does - drop it if detectDrops enabled
                keyChanges.push('DROP PRIMARY KEY');
            }
        }

        // 5. Missing Indexes (excluding PRIMARY KEY which is handled above)
        const srcIndexes = srcTable.indexes
            .filter(idx => !this._isPrimaryKey(idx))
            .map(idx => ({ raw: idx, norm: this._normalizeIndex(idx) }));
        const dstIndexes = dstTable.indexes
            .filter(idx => !this._isPrimaryKey(idx))
            .map(idx => ({ raw: idx, norm: this._normalizeIndex(idx) }));
        const dstIndexesNorm = dstIndexes.map(idx => idx.norm);

        for (const idxObj of srcIndexes) {
            if (!dstIndexesNorm.includes(idxObj.norm)) {
                this.stats.indexesAdded++;
                changes.push(`ADD ${idxObj.raw}`);
            }
        }

        // 5b. Dropped Indexes (in dest but not in source) - if enabled
        if (this.options.detectDrops) {
            const srcIndexesNorm = srcIndexes.map(idx => idx.norm);
            for (const idxObj of dstIndexes) {
                if (!srcIndexesNorm.includes(idxObj.norm)) {
                    // Extract index name for DROP INDEX statement
                    const indexName = this._extractIndexName(idxObj.raw);
                    if (indexName) {
                        changes.push(`DROP INDEX \`${indexName}\``);
                    }
                }
            }
        }

        // 6. Missing Foreign Keys (separate handling for better control)
        const srcForeignKeys = srcTable.foreignKeys || [];
        const dstForeignKeys = (dstTable.foreignKeys || []).map(fk => ({ raw: fk, norm: this._normalizeIndex(fk) }));
        const dstForeignKeysNorm = dstForeignKeys.map(fk => fk.norm);

        for (const fk of srcForeignKeys) {
            const fkNorm = this._normalizeIndex(fk);
            if (!dstForeignKeysNorm.includes(fkNorm)) {
                this.stats.indexesAdded++;
                changes.push(`ADD ${fk}`);
            }
        }

        // 6b. Dropped Foreign Keys (in dest but not in source) - if enabled
        if (this.options.detectDrops) {
            const srcForeignKeysNorm = srcForeignKeys.map(fk => this._normalizeIndex(fk));
            for (const fkObj of dstForeignKeys) {
                if (!srcForeignKeysNorm.includes(fkObj.norm)) {
                    const fkName = this._extractForeignKeyName(fkObj.raw);
                    if (fkName) {
                        changes.push(`DROP FOREIGN KEY \`${fkName}\``);
                    }
                }
            }
        }

        // Combine key changes and deferred AUTO_INCREMENT modifications at the end
        let filteredDeferred = deferredAutoIncrementModifications;
        if (pkColumnChangeManual && srcPrimaryKey) {
            const pkCols = new Set(
                this._extractPrimaryKeyColumns(srcPrimaryKey).map(c => c.toLowerCase())
            );
            filteredDeferred = deferredAutoIncrementModifications.filter(stmt => {
                const m = stmt.match(/^\s*MODIFY\s+COLUMN\s+[`"]?(\w+)[`"]?/i);
                if (!m || !m[1]) return true;
                return !pkCols.has(m[1].toLowerCase());
            });
        }
        const tailChanges = [...keyChanges, ...filteredDeferred];

        const hasAnyChanges = changes.length > 0 || tailChanges.length > 0;
        if (pkColumnChangeManual && !hasAnyChanges) {
            this.migrationScript.push(
                '-- (No other column/index/FK differences; only PRIMARY KEY differs — migrate PK manually as above.)'
            );
            this.migrationScript.push('');
            return;
        }
        if (hasAnyChanges) {
            // Some MySQL builds can fail with "Unknown column" if a PK references a column
            // being added in the same ALTER TABLE. If the new PK uses a newly-added column,
            // split into two statements:
            //  1) add/modify columns (including adding the PK column without AUTO_INCREMENT)
            //  2) drop/add PK and then add AUTO_INCREMENT via MODIFY
            // MySQL can reject a single ALTER that combines ADD COLUMN with ADD PRIMARY KEY on that
            // new column (ERROR 1054 Unknown column). Split whenever we mix ADD COLUMN with PK ops.
            // Also split when _primaryKeyUsesNewColumn detects a new PK column (covers edge cases).
            const hasAddColumn = changes.some(ch => /^\s*ADD\s+COLUMN\b/i.test(ch));
            const shouldSplitForPk =
                tailChanges.length > 0 &&
                changes.length > 0 &&
                keyChanges.length > 0 &&
                (hasAddColumn || this._primaryKeyUsesNewColumn(srcPrimaryKey, dstTable, changes));

            if (shouldSplitForPk) {
                if (changes.length > 0) {
                    this.migrationScript.push(`-- Changes for table \`${tableName}\``);
                    this.migrationScript.push(`ALTER TABLE \`${tableName}\``);
                    this.migrationScript.push(changes.map(c => '  ' + c).join(',\n') + ';');
                    this.migrationScript.push('');
                }

                this.migrationScript.push(`-- Key changes for table \`${tableName}\``);
                this.migrationScript.push(`ALTER TABLE \`${tableName}\``);
                this.migrationScript.push(tailChanges.map(c => '  ' + c).join(',\n') + ';');
                this.migrationScript.push('');
            } else {
                const allChanges = [...changes, ...tailChanges];
                this.migrationScript.push(`-- Changes for table \`${tableName}\``);
                this.migrationScript.push(`ALTER TABLE \`${tableName}\``);
                // Indent and join with commas
                const indentedChanges = allChanges.map(c => '  ' + c).join(',\n');
                this.migrationScript.push(indentedChanges + ';');
                this.migrationScript.push('');
            }
        }
    }

    _normalizeType(typeStr) {
        return typeStr.toLowerCase().replace(/\s+/g, '').replace(/[`"']/g, '');
    }

    _stripAutoIncrement(definition) {
        return String(definition)
            .replace(/\bAUTO_INCREMENT\b/ig, '')
            .replace(/\s+/g, ' ')
            .trim();
    }

    _extractPrimaryKeyColumns(primaryKeyStr) {
        if (!primaryKeyStr) return [];
        const upper = primaryKeyStr.toUpperCase();
        const pkPos = upper.indexOf('PRIMARY KEY');
        const afterPk = pkPos >= 0 ? primaryKeyStr.substring(pkPos) : primaryKeyStr;
        const cols = this._extractFirstParenthesized(afterPk);
        if (!cols) return [];
        return this._normalizeColumnList(cols)
            .split(',')
            .map(s => s.trim())
            .filter(Boolean);
    }

    _formatPrimaryKeyColumnList(primaryKeyStr) {
        const cols = this._extractPrimaryKeyColumns(primaryKeyStr);
        if (cols.length === 0) return '(?)';
        return '(' + cols.map(c => `\`${c}\``).join(', ') + ')';
    }

    _emitManualPrimaryKeyMigrationNotice(tableName, dstPrimaryKey, srcPrimaryKey) {
        const dstCols = this._formatPrimaryKeyColumnList(dstPrimaryKey);
        const srcCols = this._formatPrimaryKeyColumnList(srcPrimaryKey);
        this.migrationScript.push('-- ----------------------------------------------------------------');
        this.migrationScript.push(`-- [MANUAL PRIMARY KEY MIGRATION] Table \`${tableName}\``);
        this.migrationScript.push(`-- Current (destination) PRIMARY KEY columns: ${dstCols}`);
        this.migrationScript.push(`-- Target (source) PRIMARY KEY columns:     ${srcCols}`);
        this.migrationScript.push('-- The PRIMARY KEY column set differs (rename, surrogate id, or composite change).');
        this.migrationScript.push('-- That needs data migration and often FK / app updates; auto DROP/ADD PRIMARY KEY is skipped.');
        this.migrationScript.push('-- Apply other changes below first, then migrate the PK yourself when ready.');
        this.migrationScript.push('-- ----------------------------------------------------------------');
        this.migrationScript.push('');
    }

    _primaryKeyUsesNewColumn(srcPrimaryKey, dstTable, plannedChanges) {
        if (!srcPrimaryKey) return false;
        const pkCols = this._extractPrimaryKeyColumns(srcPrimaryKey);
        if (pkCols.length === 0) return false;

        const addedCols = new Set();
        for (const ch of plannedChanges) {
            const m = ch.match(/^\s*ADD\s+COLUMN\s+[`"]?(\w+)[`"]?\s+/i);
            if (m && m[1]) addedCols.add(m[1].toLowerCase());
        }

        const dstColKeysLower = dstTable?.columns
            ? new Set(Object.keys(dstTable.columns).map(k => k.toLowerCase()))
            : new Set();

        for (const col of pkCols) {
            const colLower = col.toLowerCase();
            if (dstColKeysLower.has(colLower)) continue;
            if (addedCols.has(colLower)) return true;
        }
        return false;
    }

    _normalizeIndex(indexStr) {
        // For comparison we want to ignore constraint/index names for PRIMARY KEY and UNIQUE keys.
        // MySQL allows different constraint/index names while still enforcing the same key columns.
        if (this._isPrimaryKey(indexStr)) {
            return this._normalizePrimaryKeyIndex(indexStr);
        }
        if (this._isUniqueIndex(indexStr)) {
            return this._normalizeUniqueIndex(indexStr);
        }
        if (this._isForeignKey(indexStr)) {
            return this._normalizeForeignKeyIndex(indexStr);
        }

        // For generic indexes, at least ignore a leading CONSTRAINT <name> wrapper.
        const withoutConstraintPrefix = indexStr.replace(/^\s*CONSTRAINT\s+[`"]?\w+[`"]?\s+/i, '');

        // MySQL often includes an optional USING clause (e.g. USING BTREE) in index definitions.
        // We treat this as non-semantic for the purposes of "already exists" detection.
        const withoutUsing = withoutConstraintPrefix.replace(/\s+USING\s+\w+/ig, '');

        return withoutUsing.toLowerCase().replace(/\s+/g, '').replace(/[`"']/g, '');
    }

    _isForeignKey(indexStr) {
        return /\bFOREIGN\s+KEY\b/i.test(indexStr);
    }

    _isPrimaryKey(indexStr) {
        // Handles both:
        //   PRIMARY KEY (`id`)
        //   CONSTRAINT `pk_name` PRIMARY KEY (`id`)
        return /\bPRIMARY\s+KEY\b/i.test(indexStr);
    }

    _isUniqueIndex(indexStr) {
        // Handles both:
        //   UNIQUE KEY `idx` (`col`)
        //   UNIQUE INDEX `idx` (`col`)
        //   UNIQUE (`col`)
        //   CONSTRAINT `idx` UNIQUE (`col`)
        const upper = indexStr.toUpperCase();
        return /\bUNIQUE\b/i.test(upper) && (/\b(KEY|INDEX)\b/i.test(upper) || /\bUNIQUE\s*\(/i.test(upper));
    }

    _normalizeColumnList(colsStr) {
        return String(colsStr)
            .toLowerCase()
            .replace(/[`"']/g, '')
            .replace(/\s+/g, '');
    }

    _extractFirstParenthesized(str, startAt = 0) {
        const s = String(str);
        const openIdx = s.indexOf('(', startAt);
        if (openIdx < 0) return null;

        let depth = 0;
        let inQuote = false;
        let quoteChar = '';

        for (let i = openIdx; i < s.length; i++) {
            const ch = s[i];

            if (['\'', '"', '`'].includes(ch)) {
                if (!inQuote) {
                    inQuote = true;
                    quoteChar = ch;
                } else if (ch === quoteChar) {
                    inQuote = false;
                }
            }

            if (inQuote) continue;

            if (ch === '(') depth++;
            else if (ch === ')') {
                depth--;
                if (depth === 0) {
                    return s.substring(openIdx + 1, i);
                }
            }
        }

        return null;
    }

    _normalizePrimaryKeyIndex(indexStr) {
        const upper = indexStr.toUpperCase();
        const pkPos = upper.indexOf('PRIMARY KEY');
        const afterPk = pkPos >= 0 ? indexStr.substring(pkPos) : indexStr;
        const cols = this._extractFirstParenthesized(afterPk);
        if (!cols) {
            // Fallback: old behaviour
            return indexStr.toLowerCase().replace(/\s+/g, '').replace(/[`"']/g, '');
        }
        return `primarykey(${this._normalizeColumnList(cols)})`;
    }

    _normalizeUniqueIndex(indexStr) {
        const upper = indexStr.toUpperCase();
        const uniquePos = upper.indexOf('UNIQUE');
        const afterUnique = uniquePos >= 0 ? indexStr.substring(uniquePos) : indexStr;
        const cols = this._extractFirstParenthesized(afterUnique);
        if (!cols) {
            // Fallback: old behaviour
            return indexStr.toLowerCase().replace(/\s+/g, '').replace(/[`"']/g, '');
        }
        return `uniquekey(${this._normalizeColumnList(cols)})`;
    }

    _normalizeForeignKeyIndex(indexStr) {
        // Goal: treat semantically equivalent FKs as equal even if:
        // - constraint name differs
        // - ON DELETE/ON UPDATE are omitted vs explicitly RESTRICT/NO ACTION
        // - whitespace / quoting differs
        //
        // We normalize to:
        //   foreignkey(<localCols>)-><refTable>(<refCols>)|delete=<action>|update=<action>
        // Where <action> is normalized and defaults to RESTRICT.

        const s = String(indexStr);
        const noConstraint = s.replace(/^\s*CONSTRAINT\s+[`"]?\w+[`"]?\s+/i, '');
        const upper = noConstraint.toUpperCase();

        const fkPos = upper.indexOf('FOREIGN KEY');
        const afterFk = fkPos >= 0 ? noConstraint.substring(fkPos) : noConstraint;

        const localColsRaw = this._extractFirstParenthesized(afterFk);
        const localCols = localColsRaw ? this._normalizeColumnList(localColsRaw) : '';

        const refMatch = afterFk.match(/\bREFERENCES\s+[`"]?(\w+)[`"]?\s*\(/i);
        const refTable = refMatch?.[1] ? refMatch[1].toLowerCase() : '';

        const refParenStart = afterFk.search(/\bREFERENCES\s+[`"]?\w+[`"]?\s*\(/i);
        const refColsRaw = refParenStart >= 0 ? this._extractFirstParenthesized(afterFk, refParenStart) : null;
        const refCols = refColsRaw ? this._normalizeColumnList(refColsRaw) : '';

        const onDeleteMatch = afterFk.match(/\bON\s+DELETE\s+(RESTRICT|CASCADE|SET\s+NULL|NO\s+ACTION)\b/i);
        const onUpdateMatch = afterFk.match(/\bON\s+UPDATE\s+(RESTRICT|CASCADE|SET\s+NULL|NO\s+ACTION)\b/i);

        const normalizeAction = (action) => {
            if (!action) return 'restrict'; // MySQL default behaviour is effectively RESTRICT/NO ACTION
            const a = String(action).toLowerCase().replace(/\s+/g, ' ').trim();
            if (a === 'no action') return 'restrict';
            return a;
        };

        const onDelete = normalizeAction(onDeleteMatch?.[1]);
        const onUpdate = normalizeAction(onUpdateMatch?.[1]);

        return `foreignkey(${localCols})->${refTable}(${refCols})|delete=${onDelete}|update=${onUpdate}`;
    }

    _extractPrimaryKey(indexes) {
        for (const idx of indexes) {
            if (this._isPrimaryKey(idx)) {
                return idx;
            }
        }
        return null;
    }

    _extractIndexName(indexStr) {
        // Try to extract index name from various formats:
        // KEY `idx_name` (...)
        // INDEX `idx_name` (...)
        // UNIQUE KEY `idx_name` (...)
        // UNIQUE INDEX `idx_name` (...)
        const match = indexStr.match(/(?:UNIQUE\s+)?(?:KEY|INDEX)\s+[`"]?(\w+)[`"]?/i);
        return match ? match[1] : null;
    }

    _extractForeignKeyName(fkStr) {
        // Extract foreign key constraint name
        // FOREIGN KEY `fk_name` (...) or CONSTRAINT `fk_name` FOREIGN KEY (...)
        const match = fkStr.match(/(?:CONSTRAINT\s+[`"]?(\w+)[`"]?\s+)?FOREIGN\s+KEY/i);
        if (match && match[1]) {
            return match[1];
        }
        // Try to extract from FOREIGN KEY `fk_name` format
        const match2 = fkStr.match(/FOREIGN\s+KEY\s+[`"]?(\w+)[`"]?/i);
        return match2 ? match2[1] : null;
    }

    _normalizeDefault(defaultValue) {
        if (defaultValue === null || defaultValue === undefined) return null;
        return String(defaultValue).toLowerCase().replace(/[`"']/g, '').trim();
    }

    getStats() {
        return this.stats;
    }
}

